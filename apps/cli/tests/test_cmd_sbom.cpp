TEST_F(SbomCmdTest, BothFormatTreatsOutputAsBasename) {
    write_cargo_lock();
    auto base = tmpdir / "mybase";
    capture_stdout([&]() {
        return cmd_sbom(ctx, {
            tmpdir.string(),
            "--format=both",
            "--output=" + base.string(),
        });
    });
    EXPECT_TRUE(fs::exists(tmpdir / "mybase.cdx.json"));
    EXPECT_TRUE(fs::exists(tmpdir / "mybase.spdx.json"));
}

TEST_F(SbomCmdTest, NoPrettyEmitsCompactJson) {
    write_cargo_lock();
    capture_stdout([&]() {
        return cmd_sbom(ctx, {tmpdir.string(), "--no-pretty"});
    });
    auto out = tmpdir / "euxis-sbom.cdx.json";
    ASSERT_TRUE(fs::exists(out));
    // Compact JSON has no newlines between fields.
    std::ifstream f(out);
    std::stringstream ss; ss << f.rdbuf();
    auto body = ss.str();
    // Single trailing newline added by write_json. Any other \n
    // would indicate pretty-printing.
    auto newlines = std::count(body.begin(), body.end(), '\n');
    EXPECT_LE(newlines, 1);
}

TEST_F(SbomCmdTest, PrettyFlagIsTheDefault) {
    write_cargo_lock();
    // No --pretty: should match --pretty=true.
    capture_stdout([&]() {
        return cmd_sbom(ctx, {tmpdir.string(), "--pretty"});
    });
    auto out = tmpdir / "euxis-sbom.cdx.json";
    ASSERT_TRUE(fs::exists(out));
    std::ifstream f(out);
    std::stringstream ss; ss << f.rdbuf();
    auto body = ss.str();
    // Pretty JSON has multiple newlines.
    auto newlines = std::count(body.begin(), body.end(), '\n');
    EXPECT_GT(newlines, 1);
}

