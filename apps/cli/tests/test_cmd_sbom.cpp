namespace fs = std::filesystem;

class SbomCmdTest : public ::testing::Test {
protected:
    fs::path tmpdir;
    fs::path cwd_before;
    Context  ctx;

    void SetUp() override {
        tmpdir = fs::temp_directory_path() /
                 ("euxis_test_sbom_" + std::to_string(::getpid()) +
                  "_" + std::to_string(reinterpret_cast<std::uintptr_t>(this)));
        fs::create_directories(tmpdir);
        // Default-output filenames are written to the *current* working
        // directory, so chdir into tmpdir to keep test output sandboxed.
        cwd_before = fs::current_path();
        fs::current_path(tmpdir);
        ctx.euxis_home = tmpdir.string();
        ctx.data_dir   = (tmpdir / "data").string();
    }

    void TearDown() override {
        fs::current_path(cwd_before);
        std::error_code ec;
        fs::remove_all(tmpdir, ec);
    }

    std::string capture_fd(int target_fd, std::function<int()> fn) {
        auto* tmp = std::tmpfile();
        if (tmp == nullptr) return {};
        int old_fd = ::dup(target_fd);
        ::dup2(::fileno(tmp), target_fd);
        fn();
        if (target_fd == STDOUT_FILENO) ::fflush(stdout);
        if (target_fd == STDERR_FILENO) ::fflush(stderr);
        ::dup2(old_fd, target_fd);
        ::close(old_fd);
        std::rewind(tmp);
        std::string out;
        char buf[4096];
        size_t n = 0;
        while ((n = std::fread(buf, 1, sizeof(buf), tmp)) > 0) {
            out.append(buf, n);
        }
        std::fclose(tmp);
        return out;
    }
    std::string capture_stdout(std::function<int()> fn) {
        return capture_fd(STDOUT_FILENO, std::move(fn));
    }
    std::string capture_cerr(std::function<int()> fn) {
        return capture_fd(STDERR_FILENO, std::move(fn));
    }

    // Drop a minimal Cargo.lock the libs/sca parser recognises.
    void write_cargo_lock() {
        std::ofstream{tmpdir / "Cargo.lock"} <<
            "# This file is automatically @generated by Cargo.\n"
            "version = 3\n"
            "\n"
            "[[package]]\n"
            "name = \"serde\"\n"
            "version = \"1.0.197\"\n"
            "source = \"registry+https://github.com/rust-lang/crates.io-index\"\n"
            "checksum = \"3fb1c7a9d4d9ad9d4b0c84d2f9e2b3a4f5c6d7e8f9a0b1c2d3e4f5a6b7c8d9e0\"\n"
            "\n"
            "[[package]]\n"
            "name = \"euxis-fixture\"\n"
            "version = \"0.1.0\"\n"
            "dependencies = [\n"
            " \"serde\",\n"
            "]\n";
    }
};

