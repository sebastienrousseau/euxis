TEST_F(SbomCmdTest, CycloneDxIncludesParsedComponent) {
    write_cargo_lock();
    capture_stdout([&]() {
        return cmd_sbom(ctx, {tmpdir.string()});
    });
    std::ifstream f(tmpdir / "euxis-sbom.cdx.json");
    nlohmann::json j = nlohmann::json::parse(f);
    EXPECT_EQ(j["bomFormat"], "CycloneDX");
    EXPECT_EQ(j["specVersion"], "1.6");
    ASSERT_TRUE(j["components"].is_array());
    // Cargo.lock fixture defines two packages.
    EXPECT_GE(j["components"].size(), 1u);
}

TEST_F(SbomCmdTest, SpdxEmitsValidJsonLd) {
    write_cargo_lock();
    capture_stdout([&]() {
        return cmd_sbom(ctx, {tmpdir.string(), "--format=spdx"});
    });
    std::ifstream f(tmpdir / "euxis-sbom.spdx.json");
    nlohmann::json j = nlohmann::json::parse(f);
    // SPDX 3.0.1 ships as JSON-LD: @context + @graph at the top level.
    ASSERT_TRUE(j.contains("@context"));
    ASSERT_TRUE(j.contains("@graph"));
    ASSERT_TRUE(j["@graph"].is_array());
    EXPECT_FALSE(j["@graph"].empty());
}

TEST_F(SbomCmdTest, EmptyTargetSucceedsWithZeroComponents) {
    // No lockfiles → scan succeeds with empty manifests vector.
    auto out = capture_stdout([&]() {
        return cmd_sbom(ctx, {tmpdir.string()});
    });
    EXPECT_TRUE(fs::exists(tmpdir / "euxis-sbom.cdx.json"));
    EXPECT_NE(out.find("0 component"), std::string::npos);
}

